	<h1>Premieres</h1>
	<p>In addition to arranging all our own music, we also expand the steelpan repertoire by writing brand new music. Every spring, members of Calypso premiere original compositions (usually chamber works for small ensembles) at Calypso's annual spring show. In the past these have ranged from new songs in the style of traditional Calypso music to unorthodox uses of the steelpan to coreographed performance works. Here is a selection of them:</p>

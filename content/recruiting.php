  <h1>Join Cardinal Calypso</h1>
    <p>Cardinal Calypso holds auditions once per year at the beginning of fall quarter. If you love music and sunshine and want to be a part of a friendly and exciting student group, we would love for you to try out.
    <ul>
      <li>No previous steel drum experience is required or expected, but bring your best musicianship!</li>
      <li>We meet for four hours a week (tentatively Sundays 4-6pm and one other weekday in the evening).</li>
      <li>We play gigs a few times a month around campus and the Bay Area.</li>
      <li>We put on a rocking spring show every year featuring original music and crazy costumes.</li>
    </ul>
    </p>
    <p>
      Are you a drummer looking for a unique musical opportunity? Sign up for an audition! Desirements: experience playing with a group and an appreciation for calypso music. 
    </p>

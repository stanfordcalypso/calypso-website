								<div class="content">
							<p>Cardinal Calypso plays the
								<a target='_blank' href='http://en.wikipedia.org/wiki/Steelpan'>steelpan</a>,
								the only acoustic instrument to be invented in the twentieth
								century. Developed in the 1940s on the Caribbean island of
								Trinidad, it is a pitched percussion instrument constructed
								from a 55-gallon oil drum. Traditionally, the steelpan is
								associated with calypso music, the afro-caribbean style which
								first evolved as a way of spreading news and free speech under
								corrupt British rule. Cardinal Calypso promotes this rich culture
								by playing calypso music in addition to other popular songs.</p>
							<p>We buy most of our instruments from Gill's Pan Shop in Curepe,
								Trinidad. They are tuned by Joe Kelly of
								<a href='http://www.californiapercussion.com' target='_blank'>
									California Percussion</a>.</p>
